<p><strong>From Roger Nystrom:</strong><br>
The second game (translated as <em>Great Demon King's Revival</em>) introduces us not only to Piccolo Daimaou but also to the Battle Card system, used in the rest of the DB games for the Famicom.</p>

<p>The game contains a unique adventure system when Gokuu is in various locales like the Kame House. More specifically, Gokuu can maintain an inventory, search,
talk, examine items, and move around in an area. Through this, Gokuu and get clues and necessary items in order to proceed. This, however, also means that
Japanese knowledge is more necessary.</p>

<p>As he finishes one area, the road ahead of him is protrayed as a game board, and Gokuu selects cards marked with numbers that act like dice. As Gokuu lands on various spaces, Kamesennin asks him to pick from face-down cards; some are bonuses; some are mini-games. However, a number of them are Piccolo cards which
cause one of the Daimaou's hordes to fight you including his henchmen Cymbal and Tambourine.</p>

<p>When Goku gets attacked, the battle cards are used once more; this time the object being is to deal the strongest cards. The fight is shown in little animated panels, and the process is repeated until one person remains standing.</p>		</td>
