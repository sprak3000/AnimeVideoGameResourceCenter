This shooter takes place in the <em>Macross II</em> OVA universe.  This game puts
a unique twist on the shooter genre.  Instead of having a certain number of ships, you have unlimited life for each stage.  The twist is that each stage has
a point total you must achieve to clear it.  If you do not rack up the necessary
amount of points, the game is over.

<p>Various power-ups transform you between fighter mode and gerwalk mode (no battroid mode that I have found so far).  You can also pick up standard missles or homing missles.  Each enemy or waves of enemies you destroy causes a coin to
appear.  Each coin is worth varying amounts of points.  To help you rack up even
more points, certain parts in each stage can be shot to reveal a bonus items.

<p>There are three levels of difficulty you can choose from; each difficulty level consists of three stages.  Each level of difficulty also has the same final, fourth stage bringing the total number of unique stages to ten.  You can play a two player co-op game, or you can choose a two player versus game.  In the versus game, you select one stage from any of the nine stages; whoever has the most points at the end of the stage is the winner.		</td>
