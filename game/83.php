<p>This is the very first Dragon Ball Game, and the title translates as The Secret of Shen Long (Holy Dragon). The game follows the now famous mountain-boy 
Gokuu as he and genius-girl Bulma look for the seven Dragon Balls; which, when put together, summons Shen Long and allows one wish to be made.</p>

<p>Gokuu loses power when touched by enemies, but also loses power over time (supposedly this is hunger kicking in). To counter this, Gokuu can find various candies or snacks when he defeats enemies. He can also find different types of staffs (one normal, one which makes Gokuu a spinning dervish), panties (makes you run faster), and keys. If you can't get anywhere, you should try to defeat enemies or bust open pots for keys.</p>

<p>The game play is very sporadic. Sometimes it's a complete mystery as to what you're to do next, and the hunger factor adds in even more frustration. However, the simple novelty of this being true to the Dragon Ball storyline may be enough for some people.</p>

<p>When you see a shell show up on the top of the screen, this means you can do a Kamehameha. To do this, hold down the <strong>B button</strong>, and then press <strong>A button</strong>.  - Roger Nystrom</p>		</td>
