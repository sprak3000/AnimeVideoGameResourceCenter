You play a young boy named Wataru Ikusabe (Keith Courage), who is teleported from his own planet to another dimension called Shinbu-Ka, to a mountain where 
the Gods of that dimension live. This mountain is under attack by Mashin demons, and Wataru's job is to slay all of the demons on this mountain and save the Gods' 
home from destruction. 

<p>The first part of the game deals with Wataru fighting demons as himself with a rather puny sword. This often gets him coins, and he can go back and forth to areas to 
get more coins as the enemies constantly reappear. With the coins he can buy shots for the upcoming stage, where he boards a robot called Ryujinmaru (Nova 
Suit). There are several shops, some of which you need to visit at one time; a girl named Himiko (Nurse Nancy) will heal you, an old man will sell you shots for your 
robot, and Toraoh (Fat guy) will sell you a new sword for the upcoming stage. 

<p>At the end of Wataru's fighting stage, there will be a Rainbow transport, where he will awaken Ryujinmaru (Nova Suit). He must go through the seven levels of the
mountain to win the game. - Roger Nystrom

<p><strong>Debug Mode:</strong> At title screen, hold <strong>1, 2, Select</strong> and <strong>Run</strong>. When the Start menu choice appears, press <strong>Up</strong> eight times (while still holding <strong>1, 2, Select, Run</strong>). - Mike Koos

