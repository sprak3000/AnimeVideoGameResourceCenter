Despite the poor graphics, this game is very entertaining. You must retrieve three items (a ribbon, a hat, and some gloves) to have Akane appear. Once Akane 
appears, you must tag her to complete the level. In order to get these items, you must push around boxes, rocks, and logs to get to them. Boxes and rocks can be 
broken, but to remove logs, you must push two or more of them togther. Of course, Ranma's rivals and girlfriends are chasing him around trying to stop him. 

<p>The puzzles are quite challenging at times. Boxes can be broken open to reveal hot or cold water. These can change you back and forth from male to female Ranma. 
Male Ranma causes objects to move until they hit something else. Female Ranma can only move objects one spot at a time. You must figure out when each form is 
appropriate. With 50 different puzzles to solve, you won't lose interest in this one for awhile. Besides the poor graphics, my only other complaint is that there is no 
password or saving options. You have three continues at your disposal, but no way to stop the game and come back to it later.		</td>
