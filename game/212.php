					<ul>						<li>Cinematic Fighter</li>						<li>Quiz Game</li>					</ul>			</ul>As best as I can translate, the title means <em>Status Play Dialogue</em>.  It is a strategy game of an undescribable type.  
You play Ranma in the story mode, who goes around different parts of Japan and meets two types of people; the first are 
ones that have various reasons for beating the living snot out of Ranma (for example, in the first stage, there's the 
Principal, Kodachi, Kunou, Gosunkugi, and Ryouga), and characters which will ask you questions for points.

<P>When you encounter battle characters, they will have a small dialogue and then you will be at a screen showing both 
your picture and your opponent's pictures, special move meters, and life meters.  Pressing <strong>A button</strong> will start a 
slot-machine-esque box <em>(Lucky Panel)</em> up, and pressing <strong>A button</strong> again stops it. This decides your luck in 
the match.  Sunny is the best, an umbrella means iffy, and a lemon means you're the farthest from having any luck of the 
sort.  Next will pop up your various battle attributes - the two top are <em>Power</em> and <em>Special Moves</em>, and the 
bottom two are <em>Defense</em> and <em>Life</em>.  Pressing <strong>B button</strong> and <strong>A button</strong> will lower and raise 
the various attributes, depending on how many points you have.  If you are satisfied with the adjustments, move to the 
first bottom Japanese word and hit <strong>A button</strong>; choosing the other one will reset the attributes.  A menu will then 
show up, letting you select various moves, which are abbreviated with <em>DF</em> (defend), <em>AT</em> (attack), and 
<em>SAT</em> (Super Attack).  Super attacks are the strongest but take off a ton off your meter.  

<P>Attacks are potent, but will do nothing if countered with a super attack.  The battle will begin when you press 
<strong>A button</strong> to move to the selections (<em>Fight</em> or <em>Reset</em>); your first move will be paired up with your 
opponent's first; your second with his/her second, and so on until either all moves are done, or until someone runs out of 
life.  The reward for winning is more points for your battle attributes.

<P>The question characters (Akane, Shampoo, and the like) will ask questions that pertain to Ranma knowledge; this is 
where even a little Japanese knowledge helps.  Typical questions range from 
&quot;Which word describes Akane's cooking&quot; (Warui - bad) to &quot;Which character first kissed Ranma?&quot; (Mikado) The object is 
to answer six questions correctly before time runs out; each time you're wrong you are penalized with more time 
subtracted.  The reward is again, more points.

<P>It helps in this game to scout out the town, remembering which houses have what.  Go for all of the question charaters 
first; even if your Japanese knowledge is nonexistent, try going for Kasumi or Shampoo in the first stage for example, 
where you'll have a 50/50 shot of being right, and your continues are unlimited, so you can go at it as long as it takes.  
In the first stage, go for Gosunkugi and the Principal and be sure to have your power boosted up high (A hundred or 
more is excellent!)  All characters except Kodachi and very few others always seem to block on the third move; use this 
to your advantage. 

<P>The third option on the main menu (Versus) works for two players.  Each taking turns planning out his/her character of choice (even Tsubasa is in this 
one!), luck, attributes, and moves. While this isn't really a game that requires skill, it can test how well you can predict and outwit your opponent. - Roger Nystrom		</td>
