People who bought or rented this will recognize the moves and play, and even the English lettering, in this first Ranma 1/2 
title (yes, you knew that clown's spinning looked familiar).  In this game, whose title means <em>Neighborhood Combat</em>, 
Ranma enters a tournament for the prize of <em>seeing China</em>.  He believes that, if he wins, he can revisit China and get 
rid of his curse of changing gender when hit with cold water.  However, there are others who either want the prize 
themselves or just want to beat up Ranma.  He battles through six enemies (Genma, Kodachi, Kuno, Principal, Shampoo, 
and Ryouga), deciding whether to battle as a boy or girl. There are two bonus stages (missing from the American version) 
where you fight Happosai and Cologne in the Tendou training hall, hitting them as many times as you can.  Your reward for 
winning the tournament is , well, seeing China alright.  I won't spoil the Ranma-esque ending, but just think of 
<em>Ranma and Juliet</em>, and how that episode ended.

<P>Control is easier than most SF-like games (too simplistic for some).  Pressing <strong>X button</strong> will unleash a special 
attack, but you can execute other moves by pressing punch and kick together, or pressing punch rapidly, or jumping 
away from an opponent or straight up and hitting <strong>X button</strong> (only some of these work on certain characters).  In 
addition, you have various punches and kicks by pressing a direction and the attack button.

<P>To get an enhanced VS. mode selection, hold down <strong>L button</strong> and <strong>R button</strong> and then move the cursor to 
VS (first option).  Hit <strong>Start</strong>, and then let go of <strong>L button</strong> and <strong>R button</strong>.  You can choose whether 
the opponents are computer or human (and if they are computer, their play attributes), the stage with <strong>Select</strong>, and 
best of all, you can not only play the same character but now Happosai and Cologne are at your disposal!

<P>To get extra credits in Story Mode, go to <strong>Options</strong>, and go to <strong>Credits</strong>.  Press <strong>Select</strong> repeatedly 
until the credit counter turns to 50.  Overall, this game is too short, but is a fun piece of Ranma merchandise.  In my 
opinion its graphics are better than <em>Hard Battle</em>'s, even though the characters are smaller, and it's a shame we didn't get 
to see the real game here in the US. - Roger Nystrom		</td>
