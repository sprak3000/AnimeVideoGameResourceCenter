This is a fighting game that pits Ultraman against his evil monster foes.  There are special attacks that you can use 
against your opponent, but I have not figured out how to block special attacks (the monsters have blocked mine, so I am 
guessing you can block their special attacks).  Here is a list of the moves I have found:

<UL>
<LI> <strong>A+B:</strong>  Fire an energy beam
<LI> <strong>Down+Forward:</strong>  Roll towards the enemy
<LI> <strong>Down+Back:</strong>  Do a backflip
<LI> <strong>Down+B:</strong>  Put up an energy shield
<LI> <strong>Up+Back+C:</strong>  Super jump backwards
<LI> <strong>Up+Forward+C:</strong>  Super jump forward
</UL>

The energy beam you fire works the same way as the SNES Ultraman game.  A meter at the top of the screen goes thru 
L1 - L4. Hitting the monster with L4 when <strong>Finish</strong> flashes will end the level.  As the power meter increases, 
<strong>Down+B</strong> loops the L1 - L4 counter. - Don Switzer		</td>
